score = int(input("whats your score?"))

if score >=90 and score <= 100:
    print ("you got a A")

elif score >=80 and score < 90:
    print ("you got a B") 

elif score >=70 and score < 80:
    print ("you got a C")
elif score >=60 and score < 70:
    print ("you got a D") 
else :
    print ("you got a F")
    